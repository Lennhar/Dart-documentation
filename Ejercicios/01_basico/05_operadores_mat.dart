/**
 * Un operador es un simbolo que le dice al compilador
 * que debe realizar una tarea
 * matemática, relacional o lógica
 * y debe producir un resultado
 */
main() {
  int a = 10 + 5; // + 15
  a = 20 - 10; // -10
  a = 10 * 2; // *20

  double b = 10 / 2; // /5
  b = 10.0 / 3; // % 1 el sobrante de la division
  b = -b; //cambia el valor de la expresion

  int c = 10 ~/ 3; //División normal da 3

  int d = 1;

  d++;
  d--;
  d += 2;
  d -= 2;
}
